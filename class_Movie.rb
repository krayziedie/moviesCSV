class Movie
	def initialize (dvd_title,studio,released,status,sound,versions,price,rating,year,genre,aspect,upc,dvd_releasedate,id,timestamp)
		@dvd_title = dvd_title	
		@studio = studio
		@released = released
		@status = status
		@sound = sound
		@versions = versions
		@price = price
		@rating = rating
		@year = year
		@genre = genre
		@aspect = aspect
		@upc = upc
		@dvd_releasedate = dvd_releasedate
		@id = id
		@timestamp = timestamp 
end

